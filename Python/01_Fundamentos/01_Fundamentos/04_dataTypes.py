
# Algunos tipos de dato en python
x = 10 # int
y = 5.678 # float
print()
print(type(x))
print(type(y))

print(x + x)
print(x + y)
print(y + y)

# Booleanos, sólo pueden tomar 1 de 2 valores True o False. Representan estados como estar encendido o apagado.
is_true = True
is_false = False
print(is_true)
print(type(is_true))

# Python también soporta notación científica

z = 1.17E6 # el uso de E o e es indistinto
a = 1.2E-6

print(z)
